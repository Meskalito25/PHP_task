<?php

// 7. Обчислення факторіалу числа - треба написати скрипт,
// який рахує факториал числа за допомогою цикла while


function factorial($n) {
    // Якщо число менше або дорівнює 1, факторіал дорівнює 1
    if ($n <= 1) {
        return 1;
    }

    $result = 1;  // Початкове значення результату
    while ($n > 1) {  // Цикл продовжується, поки $n більше 1
        $result *= $n;  // Множимо поточне значення result на $n
        $n--;  // Зменшуємо $n на 1
    }

    return $result;
}

$number = 5;  // Вкажіть число, факторіал якого потрібно обчислити
echo "Факторіал числа $number дорівнює: " . factorial($number);

